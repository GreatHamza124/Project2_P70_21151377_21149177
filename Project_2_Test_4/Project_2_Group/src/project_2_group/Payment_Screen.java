    //Handles the "Finish Payment" button action. It checks if the entered card ID and CVV match the predefined values and displays a message accordingly.
    private void payment_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payment_buttonActionPerformed
        
        try{
            int cardId_input = Integer.parseInt(cardid_enter.getText());
            int cvv_input = Integer.parseInt(cvv_enter.getText());
            if (cardId_input == cardID){
            if (cvv_input == cvvNum){
                JOptionPane.showMessageDialog(this, "Payment Successfull");
                end_program();
            } else{
                JOptionPane.showMessageDialog(this, "Incorrect cvv number");
            }
            }else{
            if (cvv_input == cvvNum){
                JOptionPane.showMessageDialog(this, "Incorrect cardID");
            }else{
                JOptionPane.showMessageDialog(this, "Incorrect cardID and cvv number");
            }
        }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Incorrect cardID and cvv number");
        }
  
        
        

    }//GEN-LAST:event_payment_buttonActionPerformed

