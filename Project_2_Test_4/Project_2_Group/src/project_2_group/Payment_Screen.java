    //Calculates the total price of items in the shopping cart and updates the total_label to display it.
    private void calculate_total(){
        String total_price_query = "SELECT SUM(price * quantity) AS total_price FROM shoppingcart";
        ResultSet total_price_set = MainProgram.shopping_cart.executeQuery(total_price_query);
        try{
            while(total_price_set.next()){
                double total_price = total_price_set.getDouble("total_price");
                String string_total = String.format("%.2f", total_price);
                total_label.setText("Total: $" + string_total);
            }
            
        }catch(SQLException e){
            e.printStackTrace();
            total_label.setText("Total: Error");
        }
    }
    
